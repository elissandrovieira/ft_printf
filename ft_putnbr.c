#include "ft_printf.h"

void	ft_putnbr(long int n)
{
	char	cn;

	if (n < 0)
	{
		n = n * -1;
		write(1, "-", 1);
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	if (n < 10)
	{
		cn = n + 48;
		write(1, &cn, 1);
	}
}
